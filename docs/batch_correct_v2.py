def apply_rules(text):
    """应用所有校对规则, 返回 (新文本, 统计)"""
    stats = {}
    for pattern, replacement, ctx, desc in RULES:
        if isinstance(replacement, str):
            new_text, n = re.subn(pattern, replacement, text)
        else:
            new_text, n = re.subn(pattern, replacement, text)
        if n > 0:
            stats[desc] = n
            text = new_text
    return text, stats


def main():
    files = sorted(ORIG.glob("校后_*.txt"))
    print(f"找到 {len(files)} 个校后文件, 应用 {len(RULES)} 条 OCR 修订规则...")
    print("=" * 70)

    grand_total = 0
    for fp in files:
        text = fp.read_text(encoding="utf-8", errors="ignore")
        new_text, stats = apply_rules(text)
        if stats:
            print(f"\n📖 {fp.name}:")
            total = sum(stats.values())
            for desc, n in sorted(stats.items(), key=lambda x: -x[1]):
                print(f"   {desc}: {n} 处")
            grand_total += total
            # 写到校后_v2 文件
            out_path = fp.parent / fp.name.replace("校后_", "校后_v2_")
            with open(out_path, "w", encoding="utf-8") as f:
                f.write(new_text)
            print(f"   ✓ 写入: {out_path.name} ({len(text):,} → {len(new_text):,})")

    print("\n" + "=" * 70)
    print(f"总计修正: {grand_total} 处")
    print("=" * 70)


if __name__ == "__main__":
    main()
